window.sr = ScrollReveal({ reset: true })

sr.reveal('.area-1, { duration: 1500}')




$("nav a, .btn").on("click", function (event) {
    if (this.hash !== "") {
        event.preventDefault();

        const hash = this.hash;

        $("html, body").animate(
            {
                scrollTop: $(hash).offset().top - 100
            },
            800
        );
    }
});